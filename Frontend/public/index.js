function get() {
    var getst=document.getElementById('By_state');
    var li = document.getElementById("suggestions");
    var stsrc = ''
    for(var i = 0; i < monuments.length; i++) {
        if(monuments[i].state == getst.value) {
            stsrc += `<option value="${monuments[i].monumentName}">${monuments[i].monumentName}</option>`;
        }
    }
    li.innerHTML = stsrc;
};

async function clicked() {
    const fname = document.getElementById('fn');
    const lname = document.getElementById('lname');
    const ph = document.getElementById('phone');
    const place = document.getElementById('drop');
    const date = document.getElementById('Dateholder');
    const people = document.getElementById('demoInput');
    const nat = document.getElementById('nat');

    var indian = true;
    if(nat.value != "indian") {
        indian = false;
    }
    
    var id = 0;
    for(var i = 0; i < monuments.length; i++) {
        if(monuments[i] == place.value) {
            id = monuments[i].id;
        }
    }

    localStorage.setItem("name", `${fname.value} ${lname.value}`);
    localStorage.setItem("phonenumber", ph.value);
    localStorage.setItem("guests", people.value);
    localStorage.setItem("id", id);
    localStorage.setItem("indian", indian);
    localStorage.setItem("date", date.value);
};